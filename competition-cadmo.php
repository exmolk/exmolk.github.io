<h4>PARTICIPANTS </h4>
<p>Competition is open for the students of public and private sector Universities, Institutes and technical colleges in Sri Lanka as groups including two members each. Participants have to confirm their studentship of the respective university or the institute by providing valid identity cards of the university or the institute.</p>

<h4>REQUIREMENTS</h4>
<p>Participants will have to bring Solidworks 2012/2013/2014 version installed computers with a three button mouse. (2014 version is preferred). 
Participants will have to submit the details of their computers before bringing them into the university premises due to security reasons.
13A Power supplies will be provided for the Laptops. Other required fittings (Ex: Multi Plugs) will have to be supplied by yourselves.
</p>

<h4>REGULATIONS</h4>

<ul>
  <li>
    Flash drives can be used for file transfer purposes, only after confirming them to be empty at the commencement of the competition.
  </li>
  <li>
    Internet facilities cannot be used during the competition.
  </li>
  <li>
    Pre-drawn parts or parts from the design library cannot be used. If such a situation is found, no marks will be provided.
  </li>
  <li>
    The decision of the judge panel will be the final decision.
  </li>
</ul>

<h4>EVALUATION CRITERA</h4>
<ul>
<li>	Completion of the modelling and assembly. </li>
<li>	Fully defining of the model. </li>
<li>	Feature identification.</li>
<li>	Proper dimensioning according to design intension.</li>
<li>	Surface and solid modelling tools.</li>
<li>	Colour rendering and material selection.</li>
<li>	Proper naming and formation of the feature design tree.</li>
<li>	Order of selecting design tools.</li>
<li>	Final modifying.</li>
<li>	Simple animation.</li>
</ul>


